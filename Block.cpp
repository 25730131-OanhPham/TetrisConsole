Block::Block(int type, int startX, int startY) 
    : blockType(type), x(startX), y(startY) {}

char Block::getCell(int row, int col) const 
{
    if (row < 0 || row >= 4 || col < 0 || col >= 4) 
        return ' ';

    return blocks[blockType][row][col];
}
